package mascotas;

public class Mascota {

    String nombre = "";
    char genero = ' ';
    double peso = 0.0;
    double largo = 0.0;

    Mascota(String nombre, char genero, double peso, double largo) {
        this.nombre = nombre;
        this.genero = genero;
        this.peso = peso;
        this.largo = largo;
    }

}
